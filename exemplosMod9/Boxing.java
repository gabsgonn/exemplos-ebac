public class Boxing {

    public static void main(String[] args) {

        // boxing
        Boolean status = true;
        Boolean status1 = Boolean.TRUE;

        Character c = 'A';

        Integer idade = 10;

        Long cpf = Long.valueOf(12313221);
        Long cpf1 = 12321523l;


        // unboxing
        boolean status2 = Boolean.TRUE;
        char letra = Character.valueOf('A');

        int idade2 = Integer.valueOf(12);

        long cpf3 = Long.valueOf(12321);
    }
}
